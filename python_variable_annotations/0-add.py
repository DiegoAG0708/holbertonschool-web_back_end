#!/usr/bin/env python3
"""
Module 0-add
Provides a type-annotated function to add two floats.
"""


def add(a: float, b: float) -> float:
    """
    Add two floating point numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
